package ali.wordRecords;

public class Record {
    private String word;
    private int count;

    public Record() {}

    public int getCount() {
        return count;
    }
    public void setCount(int count) {
        this.count = count;
    }
    public String getWord() {
        return word;
    }
    public void setWord(String word) {
        this.word = word;
    }
}
